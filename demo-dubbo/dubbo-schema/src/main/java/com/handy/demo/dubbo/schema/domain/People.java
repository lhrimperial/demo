package com.handy.demo.dubbo.schema.domain;

/**
 * @author longhairen
 * @create 2017/8/11 0011 下午 6:00
 */
public class People {
    private String id;
    private String name;
    private int age;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
